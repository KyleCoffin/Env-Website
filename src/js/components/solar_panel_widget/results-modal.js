import React from "react";
import Modal from 'react-modal';
//https://www.w3schools.com/bootstrap/bootstrap_modal.asp

const customStyles = {
  content : {
    backgroundColor: '#F8F8F8',
    border: '0',
    borderRadius: '4px',
    bottom: 'auto',
    minHeight: '10rem',
    left: '50%',
    padding: '2rem',
    position: 'fixed',
    right: 'auto',
    top: '50%',
    transform: 'translate(-50%,-50%)',
    minWidth: '20rem',
    width: '80%',
    maxWidth: '60rem'
  }
};

export default class ResultsModal extends React.Component {

	constructor(props) {
        super();
        this.state = {};
        this.afterOpenModal = this.afterOpenModal.bind(this);
        this.closeModal = this.closeModal.bind(this);
    }

  afterOpenModal() {
    // references are now sync'd and can be accessed.
    this.subtitle.style.color = 'blue';
    this.subtitle.style.textAlign = 'center'; 
  }

  closeModal() {
    this.props.onRequestClose && this.props.onRequestClose();
  }

  render() {
    return (
      <div>
        <Modal
          isOpen={this.props.modalOpen}
          onAfterOpen={this.afterOpenModal}
          onRequestClose={this.closeModal}
          style={customStyles}
          contentLabel="Example Modal"
        >
          <button type="button" className="close" style={{color: 'red'}} onClick={this.closeModal}>X</button>
          <h2 ref={subtitle => this.subtitle = subtitle}>Results</h2>
          <div>
            <h3>Savings</h3> 
            <p>There is a {this.props.paybackPeriod} year payback period.</p> 
            <p>{this.props.message}</p>
          </div>
          <div>
            <h3>How these results were generated</h3>
            <p>This data was generated by using state energy data from the EIA including:</p>
            <ul>
              <li>Price per residential kWh</li>
              <li>Energy consumption by source for {this.props.stateName}</li>
              <li>Derived Co2 per kWh</li>
            </ul>
          </div>
        </Modal>
      </div>
    );
  }
}
